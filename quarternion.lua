function createCube()
	points = {}
	local l = 10
	local p 
	for x = -3,3 do
		for y = -3,3 do
			for z = -3,3 do
				p = {
					v = scale(l, {x, y, z}),
					c = abs(x+y+z)
				}
				table.insert(points, p)
			end
		end
	end
end

function p2d(p3d)
	local v0 = add(p3d.v, center) 
	local x2d = d * v0[1] / v0[3]
	local y2d = d * v0[2] / v0[3]
	
	return x2d, y2d
end

function zsort(p1, p2)
	return p1.v[3] > p2.v[3]
end


function TIC()

	-- compute new 4 vector
	g = {c = 1, v = {0, 0, 0}}
	if btn(0) then 
		g.c = c
		g.v = add(g.v, {-1, 0, 0})
	end
	if btn(1) then 
		g.c = c
		g.v = add(g.v, {1, 0, 0})
	end
	if btn(2) then 
		g.c = c
		g.v = add(g.v, {0, 1, 0})
	end
	if btn(3) then 
		g.c = c
		g.v = add(g.v, {0, -1, 0})
	end
	if btnp(4) then
		model = not model
	end
	if btn(5) then
		h = {c = 1, v = {0, 0, 0}}
	end
	g.v = scale(s, normalise(g.v))
	h = quarternionMultiplication(g, h)

	cls(10)

	if model then createCube() else createTeapot() end
	
	-- rotate
	for k,p in pairs(points) do
		rotateQuarternion(p, h)
	end

	table.sort(points, zsort)

	-- draw
	for k,p in pairs(points) do
		i, j = p2d(p)
		rect(i + 120, j + 68, 6, 6, p.c)
	end
	print("z : change model\nx : reset orientation")
	print("h={"..string.format("%01f",h.c)..","..string.format("%01f",h.v[1])..","..string.format("%01f",h.v[2])..","..string.format("%01f",h.v[3]).."}", 0, 130)

end

