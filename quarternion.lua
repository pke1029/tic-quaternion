function norm(v)
	return sqrt(v[1]*v[1] + v[2]*v[2] + v[3]*v[3])
end

function normalise(v)
	local r = norm(v)
	if r == 0 then 
		return {0, 0, 0} 
	else 
		return scale(1/r, v)
	end
end

function add(u, v)
	return {u[1]+v[1], u[2]+v[2], u[3]+v[3]}
end

function scale(k, v)
	return {k*v[1], k*v[2], k*v[3]}
end

function cross(u, v)
	return {u[2]*v[3]-v[2]*u[3], v[1]*u[3]-u[1]*v[3], u[1]*v[2]-v[1]*u[2]}
end

function dot(u, v)
	return u[1]*v[1] + u[2]*v[2] + u[3]*v[3]
end

function quarternionMultiplication(h, g)
	local c1 = h.c
	local c2 = g.c
	local v1 = h.v
	local v2 = g.v

	return {c = c1*c2 - dot(v1, v2), 
			v = add(add(scale(c1, v2), scale(c2, v1)), cross(v1, v2))}
end

function rotateQuarternion(p3d, h)
	local c = h.c
	local u = h.v
	local v = add(p3d.v, scale(-1, pivot))
	local s = norm(u)

	local k = dot(u, v)
	local w = cross(u, v)

	p3d.v = add(add(add(scale(2*k, u), scale(c*c-s*s, v)), scale(2*c, w)), pivot)
end

